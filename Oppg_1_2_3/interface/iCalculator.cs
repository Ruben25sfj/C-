public interface Calculator
{
   /// <summary>
   /// 
   /// </summary>
   /// <param name="a">the value of the number a</param>
   /// <param name="b">the value of the number b</param>
   /// <returns>a + b</returns>
    double Add(double a, double b);

    /// <summary>
    /// 
    /// </summary>
    /// <param name="a">the value of the number a</param>
    /// <param name="b">the value of the number b</param>
    /// <returns>a - b</returns>
    double Substract(double a, double b);

}




